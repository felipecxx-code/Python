# Faça um programa que calcule a área de um triângulo. Onde você informará a base e a altura. 

# Fórmula: A = (b x h) / 2

# Onde: A = Área

# b = base

# h = altura

# Escreva seu código aqui

b = float(input('Informe o valor da base do triângulo: '))
h = float(input('Informe o valor da altura do triângulo: '))

a = (b * h) / 2

print(f'O valor da área do triângulo é {a}')