# Faça um programa que calcule a área de um retângulo. Onde você informará o comprimento e a largura. 

# Fórmula: A = c x l 

# Onde: A = Área

# c = comprimento

# l = largura

# Escreva seu código aqui

c = float(input('Informe o valor do comprimento do retângulo: '))
